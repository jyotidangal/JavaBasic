package com.firstassignment.loops;

public class Qn20DisplayPattern {
    public static void main(String[]args){
        for(int i=0 ; i<6 ; i++)
        {
            for(int j=1;j<i*2;j++){
                System.out.print("*");
            }
            System.out.print('\n');
        }

    }
}
