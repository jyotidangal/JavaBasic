package com.firstassignment.loops;

public class Qn40DisplayNumberUsingWhile {
    public static void main(String[] args){
        int number=100;
        int i=100;
        System.out.println("Numbers from 1 to 100 are:");
        do{
            i--;
            System.out.println(number-i);

        }while(i>0);
    }
}
