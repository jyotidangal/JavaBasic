package com.firstassignment.loops;

public class Qn38InnerForLoop {
    public static void main(String[] args){
        for(int i=1; i<5;i++)
        {
            for(int j=1;j<3;j++){
                System.out.print("Hello, this is for inner loop");
            }
            System.out.println(" \n");
        }
    }
}
